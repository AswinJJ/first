package com.java_demo;

public class Swap {
	public static void main(String[] args) {
		int a = 20;
		int b = 30;
		 
		
		int c = a;//20
		a=b;
		b=c;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println("//////////////////////////////////////////////////////////");
		
		int e = 20;
		int f = 30;
		int d = e+f;//50
		f=e;
		e=d-f;
		System.out.println(e);
		System.out.println(f);
		
		
		
		
		
		
		
		
	}

}
